#!/bin/sh

STATE_FILE="/etc/dns-smart-routing/state.json"
CONF_FILE="/tmp/dnsmasq_dynamic_servers.conf"
TMP_FILE="/tmp/dnsmasq_dynamic_servers.conf.tmp"

if [ ! -f "$STATE_FILE" ]; then
    exit 0
fi

state=$(jq -r ".state // \"default\"" "$STATE_FILE")
local_dns=$(uci -q get dns-smart-routing.global.local_dns || echo "127.0.0.1#15353")

# Initialize tmp file
echo "# Autogenerated by dns_smart_apply.sh" > "$TMP_FILE"

if [ "$state" = "local" ]; then
    echo "server=$local_dns" >> "$TMP_FILE"
fi

# Compare and replace if different
if [ ! -f "$CONF_FILE" ] || ! cmp -s "$TMP_FILE" "$CONF_FILE"; then
    mv "$TMP_FILE" "$CONF_FILE"
    kill -HUP $(pidof dnsmasq)
else
    rm -f "$TMP_FILE"
fi
