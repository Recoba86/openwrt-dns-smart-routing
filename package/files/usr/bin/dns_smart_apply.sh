#!/bin/sh

STATE_FILE="/etc/dns-smart-routing/state.json"
CONF_FILE="/tmp/dnsmasq_dynamic_servers.conf"
TMP_FILE="/tmp/dnsmasq_dynamic_servers.conf.tmp"

if [ ! -f "$STATE_FILE" ]; then
    exit 0
fi

# Validate JSON before parsing
if ! jq -e . "$STATE_FILE" >/dev/null 2>&1; then
    exit 0
fi

state=$(jq -r ".state // \"NORMAL\"" "$STATE_FILE")
local_dns=$(uci -q get dns-smart-routing.global.local_dns || echo "127.0.0.1#15353")

# Initialize tmp file
echo "# Autogenerated by dns_smart_apply.sh" > "$TMP_FILE"

if [ "$state" = "FAILOVER" ]; then
    echo "server=$local_dns" >> "$TMP_FILE"
fi

# Compare and replace atomically if different
if [ ! -f "$CONF_FILE" ] || ! cmp -s "$TMP_FILE" "$CONF_FILE"; then
    mv "$TMP_FILE" "$CONF_FILE"
    
    # Reload dnsmasq safely
    if which killall >/dev/null 2>&1; then
        killall -HUP dnsmasq 2>/dev/null || true
    else
        for pid in $(pidof dnsmasq); do
            kill -HUP "$pid" 2>/dev/null || true
        done
    fi
else
    rm -f "$TMP_FILE"
fi
