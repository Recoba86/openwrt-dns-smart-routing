#!/bin/sh

STATE_FILE="/etc/dns-smart-routing/state.json"
CONF_FILE="/tmp/dnsmasq_dynamic_servers.conf"
TMP_FILE="/tmp/dnsmasq_dynamic_servers.conf.tmp"

[ ! -f "$STATE_FILE" ] && exit 0

# Validate JSON before parsing — never crash on corrupt state
if ! jq -e . "$STATE_FILE" >/dev/null 2>&1; then
    exit 0
fi

state=$(jq -r '.state // "NORMAL"' "$STATE_FILE" 2>/dev/null || echo "NORMAL")
local_dns=$(uci -q get dns-smart-routing.global.local_dns 2>/dev/null || echo "127.0.0.1#15353")

# ── BUILD NEW CONFIG INTO TEMP FILE ─────────────────────────────────────────
printf '# Autogenerated by dns_smart_apply.sh\n' > "$TMP_FILE"

[ "$state" = "FAILOVER" ] && printf 'server=%s\n' "$local_dns" >> "$TMP_FILE"

# ── ATOMIC REPLACE ONLY IF CHANGED ───────────────────────────────────────────
if [ ! -f "$CONF_FILE" ] || ! cmp -s "$TMP_FILE" "$CONF_FILE"; then
    mv "$TMP_FILE" "$CONF_FILE"

    # ── RELOAD DNSMASQ (pidof loop — no external tools assumed) ──────────────
    for pid in $(pidof dnsmasq 2>/dev/null); do
        kill -HUP "$pid" 2>/dev/null || true
    done
else
    rm -f "$TMP_FILE"
fi
