#!/bin/sh

STATE_FILE="/etc/dns-smart-routing/state.json"
FINGERPRINT_FILE="/etc/dns-smart-routing/runtime_fingerprint.json"
CONF_FILE="/tmp/dnsmasq_dynamic_servers.conf"
TMP_FILE="/tmp/dnsmasq_dynamic_servers.conf.tmp"

if [ ! -f "$STATE_FILE" ]; then
    exit 0
fi

# Validate JSON before parsing
if ! jq -e . "$STATE_FILE" >/dev/null 2>&1; then
    exit 0
fi

state=$(jq -r ".state // \"CLEAN\"" "$STATE_FILE")
local_dns=$(uci -q get dns-smart-routing.global.local_dns || echo "127.0.0.1#15353")

# Initialize tmp file
echo "# Autogenerated by dns_smart_apply.sh" > "$TMP_FILE"

if [ "$state" = "DEGRADED" ]; then
    echo "server=1.1.1.1" >> "$TMP_FILE"
    echo "server=8.8.8.8" >> "$TMP_FILE"
    echo "server=$local_dns" >> "$TMP_FILE"
elif [ "$state" = "BROKEN" ]; then
    echo "server=$local_dns" >> "$TMP_FILE"
fi

# Write atomically
mv "$TMP_FILE" "$CONF_FILE"

# Reload dnsmasq safely by iterating PIDs
for pid in $(pidof dnsmasq); do
    kill -HUP "$pid" 2>/dev/null || true
done

# Compute hash and write fingerprint file atomically
config_hash=$(md5sum "$CONF_FILE" 2>/dev/null | awk '{print $1}')
now=$(date +%s)

jq -n --arg st "$state" \
      --arg ch "$config_hash" \
      --argjson la "$now" \
      '{state: $st, config_hash: $ch, last_apply: $la}' \
      > "$FINGERPRINT_FILE.tmp"
sync
mv "$FINGERPRINT_FILE.tmp" "$FINGERPRINT_FILE"
