#!/bin/sh

STATE_FILE="/etc/dns-smart-routing/state.json"
CONF_FILE="/tmp/dnsmasq_dynamic_servers.conf"
TMP_FILE="${CONF_FILE}.tmp"
LAST_RELOAD_FILE="/tmp/dns-smart-routing.last_reload"
RELOAD_COOLDOWN=60

[ ! -f "$STATE_FILE" ] && exit 0

# Filesystem safety: validate JSON before any operation
jq -e . "$STATE_FILE" >/dev/null 2>&1 || exit 0

state=$(jq -r '.state // "NORMAL"' "$STATE_FILE" 2>/dev/null || echo "NORMAL")
local_dns=$(uci -q get dns-smart-routing.global.local_dns 2>/dev/null || echo "127.0.0.1#15353")

# ── BUILD NEW CONFIG (filesystem safety: write to tmp only) ──────────────────
printf '# Autogenerated by dns_smart_apply.sh\n' > "$TMP_FILE" 2>/dev/null || exit 0
[ "$state" = "FAILOVER" ] && printf 'server=%s\n' "$local_dns" >> "$TMP_FILE" 2>/dev/null

# ── ATOMIC REPLACE ONLY IF CONTENT CHANGED ───────────────────────────────────
if [ ! -f "$CONF_FILE" ] || ! cmp -s "$TMP_FILE" "$CONF_FILE" 2>/dev/null; then
    # Atomic mv — on failure remove tmp and exit silently
    mv "$TMP_FILE" "$CONF_FILE" 2>/dev/null || { rm -f "$TMP_FILE" 2>/dev/null; exit 0; }

    # ── RELOAD WITH 60s COOLDOWN ──────────────────────────────────────────────
    now=$(date +%s 2>/dev/null || echo "0")
    last_reload=$(cat "$LAST_RELOAD_FILE" 2>/dev/null || echo "0")
    last_reload=$(printf '%d' "$last_reload" 2>/dev/null || echo "0")

    if [ $((now - last_reload)) -ge $RELOAD_COOLDOWN ]; then
        # pidof failure → silently skip reload entirely
        for pid in $(pidof dnsmasq 2>/dev/null); do
            kill -HUP "$pid" 2>/dev/null || true
        done
        printf '%s\n' "$now" > "$LAST_RELOAD_FILE" 2>/dev/null || true
    fi
else
    rm -f "$TMP_FILE" 2>/dev/null
fi
